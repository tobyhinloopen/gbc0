const uint8_t *font_data_get(char c) {
  if (c < font_data_ascii_offset || c > font_data_ascii_max)
    return NULL;

  uint16_t i = font_data_indices[(uint8_t)c - font_data_ascii_offset];
  if (i == 0)
    return NULL;

  return &font_data[i];
}
